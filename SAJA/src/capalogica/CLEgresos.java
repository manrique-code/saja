package capalogica;

/**
 *
 * @author Carlos Aguilar
 */
public class CLEgresos {
    private int idControlEgreso, idUsuario;
    private String fechaEgreso, observacion, motivo;
    private float valor;

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

    public int getIdControlEgreso() {
        return idControlEgreso;
    }

    public void setIdControlEgreso(int idControlEgreso) {
        this.idControlEgreso = idControlEgreso;
    }

    public String getFechaEgreso() {
        return fechaEgreso;
    }

    public void setFechaEgreso(String fechaEgreso) {
        this.fechaEgreso = fechaEgreso;
    }

    public String getObservacion() {
        return observacion;
    }

    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }
    
    
}
